// Helper functions
export const findVehicleByName = (name: string): Vehicle | undefined => {
  return vehicles.find(vehicle => vehicle.name.toLowerCase().includes(name.toLowerCase()));
};

export const getAllBrands = (): string[] => {
  const brands = Array.from(new Set(vehicles.map(vehicle => vehicle.brand)));
  return brands.sort();
};

export const getVehiclesByBrand = (brand: string): Vehicle[] => {
  return vehicles.filter(vehicle => vehicle.brand === brand);
};

export const getVehiclesSortedByBrand = (): Vehicle[] => {
  return [...vehicles].sort((a, b) => a.brand.localeCompare(b.brand));
};